/**
 * Class which provides an entry point for the application. When the main() method is run, a new
 * Converter object is instantiated.
 * 
 * @author Andrew Mass
 * @version 1.0.0
 */
public class Runner {

  /**
   * Creates a new Converter object, which runs the Converter() constructor and starts the
   * application.
   * 
   * @param args
   *          Command-line arguments sent to the program. These are unused for this application.
   */
  public static void main(String[] args) {
    new Converter();
  }
}
