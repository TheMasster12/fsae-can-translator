/**
 * Class which provides an entry point for the application. When the main() method is run, a new Translator object is
 * instantiated.
 * 
 * @author Andrew Mass
 * @version 1.0.1
 */
public class Runner {

  /**
   * Creates a new Translator object, which runs the Translator() constructor and starts the application.
   * 
   * @param args Command-line arguments sent to the program. These are unused for this application.
   */
  public static void main(String[] args) {
    new Translator();
  }
}
